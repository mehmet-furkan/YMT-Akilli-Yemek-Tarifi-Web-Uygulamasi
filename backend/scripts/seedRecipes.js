async function ensureSystemUser() {
  let user = await User.findOne({ email: SYSTEM_USER_EMAIL });
  if (user) return user;
  user = await User.create({
    name: SYSTEM_USER_NAME,
    email: SYSTEM_USER_EMAIL,
    password: SYSTEM_USER_PASSWORD,
  });
  console.log(`✓ Created system user: ${user.email}`);
  return user;
}

async function main() {
  await mongoose.connect(process.env.MONGO_URI);
  console.log(`✓ Connected to MongoDB at ${mongoose.connection.host}`);

  const systemUser = await ensureSystemUser();

  const deleted = await Recipe.deleteMany({});
  console.log(`✓ Deleted ${deleted.deletedCount} existing recipes`);

  const enriched = recipes.map((r) => ({ ...r, createdBy: systemUser._id }));
  const inserted = await Recipe.insertMany(enriched, { ordered: false });
  console.log(`✓ Inserted ${inserted.length} recipes`);

  const byCategory = await Recipe.aggregate([
    { $group: { _id: "$category", count: { $sum: 1 } } },
    { $sort: { count: -1 } },
  ]);
  console.log("\nRecipes by category:");
  byCategory.forEach((c) => console.log(`  ${c._id.padEnd(15)} ${c.count}`));

  const byDifficulty = await Recipe.aggregate([
    { $group: { _id: "$difficulty", count: { $sum: 1 } } },
  ]);
  console.log("\nRecipes by difficulty:");
  byDifficulty.forEach((d) => console.log(`  ${d._id.padEnd(8)} ${d.count}`));

  await mongoose.disconnect();
  console.log("\n✅ Seed complete.");
}

main().catch((err) => {
  console.error("❌ Seed failed:", err);
  process.exit(1);
});
